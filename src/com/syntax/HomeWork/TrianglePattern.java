package com.syntax.HomeWork;

public class TrianglePattern {

	public static void main(String[] args) {
		
		 int n=7;
		 for (int i=1;i<=n; i++ ){
		   int p=1;
		 for (int j=i;j<=n;j++){
		   System.out.print(p++);
		   System.out.print(" ");
		 }
		 System.out.println();
		 }
		 int b=7;
		 for (int i=2;i<=b; i++ ){
		 for (int j=1;j<=i;j++){
		   System.out.print(j+" ");
		 }
		   System.out.println();
		 }

		
		
		
		
		
		
		
		
		
		
		
		
		
}
}
